package WEEK_01_01;

public class Calculator {
	 int result =0;

	    float add(float num){
	        result +=num;
	        return  result;
	    }

	    float sub(float num){
	        result -=num;
	        return  result;
	    }

	    float mul(float num){
	        result *=num;
	        return  result;
	    }

	    float div(float num){
	        result /=num;
	        return  result;
	    }

}
